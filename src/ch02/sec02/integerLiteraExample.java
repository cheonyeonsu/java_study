
package ch02.sec02;

public class integerLiteraExample {
 
	public static void main(String[] args) {
		int var1 = 0b1011; //2진수
		int vat2 = 0206; //8진수
		int var3 = 365;
		int var4 = 0xB3; //16진수
		
		//컨트롤 + D = 라인삭제
		//컨트롤+알트 + 방향키 = 라인복사
		System.out.println(var1);
		System.out.println(vat2);
		System.out.println(var3);
		System.out.println(var4);
		
	}
}
